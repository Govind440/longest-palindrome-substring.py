# String- Strings are a sequence of characters. They are used to
# store and manipulate text. In Python, strings are enclosed in
# either single quotes (' ') or double quotes (" ").
# It is important to note that strings in Python are immutable, meaning that
# once a string is created, it cannot be changed. However, you can create
# a new string by concatenating or slicing existing strings.
# Example of creating a string

my_string = "Hello, World!"
print(my_string)

# Example of concatenating strings

Greetings = "Hello" + ", " + "World!"
print(Greetings)
my_string = ''' This is a multi-line string.
It can span multiple lines.'''
print(my_string)

